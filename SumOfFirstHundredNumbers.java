class SumOfFirstHundredNumbers
{
    public static void main(String[] args) {
        int SumOfFirstHundredNumbers = 0;
        for(int num=1;num<=100;num++){
           SumOfFirstHundredNumbers = SumOfFirstHundredNumbers + num;
       }
       System.out.println(SumOfFirstHundredNumbers);
    }
}
